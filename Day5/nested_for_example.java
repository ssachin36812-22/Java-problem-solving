package Day5;
public class nested_for_example {
    public static void main(String[] args){
        for(int i=0;i<=5;i++) {//how many times

            for (int j = 1; j <= 5; j++) {//what to print
                //printing loop 12345
                System.out.print(i);
            }
            System.out.println(" ");

        }
    }
}